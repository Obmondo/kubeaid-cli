func (f *fakeKeycloak) handleClientRoleByName(w http.ResponseWriter, r *http.Request, realm, clientInternalID, roleName string) {
	f.mu.Lock()
	c, ok := f.clients[realm][clientInternalID]
	f.mu.Unlock()
	if !ok {
		http.Error(w, "client not found", http.StatusNotFound)
		return
	}
	if r.Method != http.MethodGet {
		http.Error(w, "method not allowed", http.StatusMethodNotAllowed)
		return
	}
	roles, ok := clientRolesFixture[derefString(c.ClientID)]
	if !ok {
		http.Error(w, "no roles", http.StatusNotFound)
		return
	}
	for _, role := range roles {
		if role.Name != nil && *role.Name == roleName {
			writeJSON(w, http.StatusOK, role)
			return
		}
	}
	http.Error(w, "role not found", http.StatusNotFound)
}

// clientRolesFixture pre-seeds roles for known clients so role
