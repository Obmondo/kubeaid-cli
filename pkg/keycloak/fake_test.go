func (f *fakeKeycloak) handleUserResetPassword(w http.ResponseWriter, r *http.Request, realm, userID string) {
	f.mu.Lock()
	defer f.mu.Unlock()

	if f.users[realm] == nil || f.users[realm][userID] == nil {
		http.Error(w, "user not found", http.StatusNotFound)
		return
	}
	if r.Method != http.MethodPut {
		http.Error(w, "method not allowed", http.StatusMethodNotAllowed)
		return
	}
	f.noteWrite()
	w.WriteHeader(http.StatusNoContent)
}

func (f *fakeKeycloak) handleUserClientRoles(w http.ResponseWriter, r *http.Request, realm, userID string, parts []string) {
	if len(parts) == 0 {
		http.NotFound(w, r)
		return
	}
	clientInternalID := parts[0]

	f.mu.Lock()
	defer f.mu.Unlock()

	if f.userClientRoles[realm] == nil {
		f.userClientRoles[realm] = make(map[string]map[string][]string)
	}
	if f.userClientRoles[realm][userID] == nil {
		f.userClientRoles[realm][userID] = make(map[string][]string)
	}

	switch r.Method {
	case http.MethodGet:
		assigned := f.userClientRoles[realm][userID][clientInternalID]
		out := []*gocloak.Role{}
		// Resolve role names to *gocloak.Role from the fixture so
		// the names + IDs round-trip cleanly.
		c, ok := f.clients[realm][clientInternalID]
		if !ok {
			writeJSON(w, http.StatusOK, out)
			return
		}
		fixture := clientRolesFixture[derefString(c.ClientID)]
		for _, name := range assigned {
			for _, role := range fixture {
				if role.Name != nil && *role.Name == name {
					out = append(out, role)
				}
			}
		}
		writeJSON(w, http.StatusOK, out)
	case http.MethodPost:
		var roles []gocloak.Role
		if err := json.NewDecoder(r.Body).Decode(&roles); err != nil {
			http.Error(w, err.Error(), http.StatusBadRequest)
			return
		}
		current := f.userClientRoles[realm][userID][clientInternalID]
		for _, role := range roles {
			if role.Name == nil {
				continue
			}
			already := false
			for _, existing := range current {
				if existing == *role.Name {
					already = true
					break
				}
			}
			if !already {
				current = append(current, *role.Name)
			}
		}
		f.userClientRoles[realm][userID][clientInternalID] = current
		f.noteWrite()
		w.WriteHeader(http.StatusNoContent)
	default:
		http.Error(w, "method not allowed", http.StatusMethodNotAllowed)
	}
}

// --- helpers ---

func writeJSON(w http.ResponseWriter, status int, body any) {
	w.Header().Set("Content-Type", "application/json")
	w.WriteHeader(status)
	_ = json.NewEncoder(w).Encode(body)
}

func derefString(p *string) string {
	if p == nil {
		return ""
	}
	return *p
}

// newTestReconciler spins up a fake Keycloak, logs in as admin,
// and returns the resulting Reconciler plus the fake (so tests can
// inspect / reset state) and the cleanup handle.
func newTestReconciler(t *testing.T) (*Reconciler, *fakeKeycloak) {
	t.Helper()

	fake := newFakeKeycloak()
	srv := httptest.NewServer(fake.handler())
	t.Cleanup(srv.Close)

	r, err := NewReconciler(context.Background(), srv.URL, "admin", "p")
	require.NoError(t, err)
	return r, fake
}
